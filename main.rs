// ── gsettings ────────────────────────────────────────────────────────────────

fn set_gsettings(dark_mode: bool) {
    let scheme = if dark_mode {
        "prefer-dark"
    } else {
        "prefer-light"
    };

    let _ = Command::new("gsettings")
        .args(["set", "org.gnome.desktop.interface", "color-scheme", scheme])
        .status();

    // Also set gtk-theme to Adwaita so our CSS overrides apply cleanly
    let gtk_theme = if dark_mode { "Adwaita-dark" } else { "Adwaita" };
    let _ = Command::new("gsettings")
        .args(["set", "org.gnome.desktop.interface", "gtk-theme", gtk_theme])
        .status();

    println!(
        "[noctalia-gtk] gsettings → color-scheme={} gtk-theme={}",
        scheme, gtk_theme
    );
}

// ── File writing ─────────────────────────────────────────────────────────────

fn write_gtk_css(colors: &NoctaliaColors, dark_mode: bool) {
    let home = std::env::var("HOME").unwrap_or_else(|_| "/root".to_string());

    let gtk3_dir = PathBuf::from(&home).join(".config/gtk-3.0");
    let gtk4_dir = PathBuf::from(&home).join(".config/gtk-4.0");

    fs::create_dir_all(&gtk3_dir).ok();
    fs::create_dir_all(&gtk4_dir).ok();

    let gtk3_css = generate_gtk3_css(colors);
    let gtk4_css = generate_gtk4_css(colors);

    fs::write(gtk3_dir.join("gtk.css"), &gtk3_css)
        .expect("Failed to write gtk-3.0/gtk.css");
    fs::write(gtk4_dir.join("gtk.css"), &gtk4_css)
        .expect("Failed to write gtk-4.0/gtk.css");

    println!(
        "[noctalia-gtk] Wrote GTK CSS ({} mode)",
        if dark_mode { "dark" } else { "light" }
    );
}

// ── Read + apply ─────────────────────────────────────────────────────────────

fn apply(colors_path: &Path, settings_path: &Path) {
    let colors_raw = match fs::read_to_string(colors_path) {
        Ok(s) => s,
        Err(e) => {
            eprintln!("[noctalia-gtk] Failed to read colors.json: {}", e);
            return;
        }
    };

    let settings_raw = match fs::read_to_string(settings_path) {
        Ok(s) => s,
        Err(e) => {
            eprintln!("[noctalia-gtk] Failed to read settings.json: {}", e);
            return;
        }
    };

    let colors: NoctaliaColors = match serde_json::from_str(&colors_raw) {
        Ok(c) => c,
        Err(e) => {
            eprintln!("[noctalia-gtk] Failed to parse colors.json: {}", e);
            return;
        }
    };

    let settings: NoctaliaSettings = match serde_json::from_str(&settings_raw) {
        Ok(s) => s,
        Err(e) => {
            eprintln!("[noctalia-gtk] Failed to parse settings.json: {}", e);
            return;
        }
    };

    let dark_mode = settings.color_schemes.dark_mode;
    write_gtk_css(&colors, dark_mode);
    set_gsettings(dark_mode);
}

// ── Main ─────────────────────────────────────────────────────────────────────

fn main() {
    let home = std::env::var("HOME").unwrap_or_else(|_| "/root".to_string());
    let noctalia_dir = PathBuf::from(&home).join(".config/noctalia");
    let colors_path = noctalia_dir.join("colors.json");
    let settings_path = noctalia_dir.join("settings.json");

    // Apply immediately on startup
    println!("[noctalia-gtk] Starting — applying current theme...");
    apply(&colors_path, &settings_path);

    // Watch for changes
    let mut inotify = Inotify::init().expect("Failed to init inotify");

    inotify
        .watches()
        .add(&noctalia_dir, WatchMask::CLOSE_WRITE | WatchMask::MOVED_TO)
        .expect("Failed to watch noctalia config dir");

    println!("[noctalia-gtk] Watching {:?} for changes...", noctalia_dir);

    let mut buffer = [0u8; 4096];
    loop {
        let events = inotify
            .read_events_blocking(&mut buffer)
            .expect("Failed to read inotify events");

        let mut changed = false;
        for event in events {
            if event.mask.contains(EventMask::CLOSE_WRITE)
                || event.mask.contains(EventMask::MOVED_TO)
            {
                if let Some(name) = event.name {
                    let name = name.to_string_lossy();
                    if name == "colors.json" || name == "settings.json" {
                        changed = true;
                    }
                }
            }
        }

        if changed {
            println!("[noctalia-gtk] Change detected — reapplying theme...");
            // Small delay to let noctalia finish writing
            std::thread::sleep(std::time::Duration::from_millis(200));
            apply(&colors_path, &settings_path);
        }
    }
}
