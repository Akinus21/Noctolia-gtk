/// noctalia-gtk
///
/// Watches ~/.config/noctalia/colors.json and settings.json for changes,
/// then writes matching GTK 3 and GTK 4 CSS overrides and updates gsettings
/// so that GTK apps (like Nyxt) follow the active Noctalia theme.
///
/// Run on login. Add to your niri/autostart or systemd user session.

use inotify::{EventMask, Inotify, WatchMask};
use serde::Deserialize;
use std::fs;
use std::path::{Path, PathBuf};
use std::process::Command;

// ── Noctalia color schema ────────────────────────────────────────────────────

#[derive(Debug, Deserialize)]
#[serde(rename_all = "camelCase")]
struct NoctaliaColors {
    m_error: String,
    m_on_error: String,
    m_hover: String,
    m_on_hover: String,
    m_on_primary: String,
    m_on_secondary: String,
    m_on_surface: String,
    m_on_surface_variant: String,
    m_on_tertiary: String,
    m_outline: String,
    m_primary: String,
    m_secondary: String,
    m_shadow: String,
    m_surface: String,
    m_surface_variant: String,
    m_tertiary: String,
}

#[derive(Debug, Deserialize)]
#[serde(rename_all = "camelCase")]
struct ColorSchemes {
    dark_mode: bool,
}

#[derive(Debug, Deserialize)]
#[serde(rename_all = "camelCase")]
struct NoctaliaSettings {
    color_schemes: ColorSchemes,
}

